CREATE OR REPLACE TRIGGER emp_count_trigger
AFTER INSERT OR DELETE OR UPDATE
ON EMPLOYEE2
DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count FROM EMPLOYEE2;
    DBMS_OUTPUT.PUT_LINE('Total number of tuples in EMPLOYEE2: ' || v_count);
END;
/
    
