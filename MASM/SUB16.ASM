ASSUME DS:DATA, CS:CODE
DATA SEGMENT
    M1 DB 10,13,"ENTER FIRST NUMBER: $"
    M2 DB 10,13,"ENTER SECOND NUMBER: $"
    M3 DB 10,13,"DIFFERENCE: $"
    M4 DB "-$"
    PROD DB 2 DUP(0)
DATA ENDS

PRTMSG MACRO MSG
    LEA DX,MSG
    MOV AH,09H
    INT 21H
ENDM
GETDCM MACRO
    MOV AH,01H
    INT 21H
    SUB AL,30H
ENDM

PRTDCM MACRO
    MOV DL,[SI]
    ADD DL,30H
    MOV AH,02H
    INT 21H
ENDM

CODE SEGMENT
START:
    MOV AX,DATA
    MOV DS,AX

    PRTMSG M1
    GETDCM
    MOV BH,AL
    GETDCM
    MOV BL,AL
    MOV AL,10
    MUL BH
    ADD AL,BL
    MOV BL,AL

    PRTMSG M2
    GETDCM
    MOV CH,AL
    GETDCM
    MOV CL,AL
    MOV AL,10
    MUL CH
    ADD AL,CL
    MOV CL,AL

    PRTMSG M3
    MOV AH,00H

    MOV AL,BL
    CMP AL,CL
    JNB IS_POS;
    PRTMSG M4
    MOV AH,00H
    XCHG BL,CL
    MOV AL,BL

IS_POS:
    SUB AL,CL

    LEA SI,PROD
    ADD SI,1
    MOV CX,2
    MOV BX,10

CONVERT_LOOP:
    MOV DX,0
    DIV BX
    MOV [SI],DL
    DEC SI
    LOOP CONVERT_LOOP
    
    LEA SI,PROD
    PRTDCM
    INC SI
    PRTDCM
    MOV AH,4CH
    INT 21H
CODE ENDS
END START
