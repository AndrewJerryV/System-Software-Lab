ASSUME DS:DATA, CS:CODE
DATA SEGMENT
    STRING DB 100 DUP(?)
    M1 DB 10,13,"FOUND$"
    M2 DB 10,13,"NOT FOUND$"
    M3 DB 10,13,"ENTER NO OF ITEMS: $"
    M4 DB 10,13,"ENTER ITEM: $"
    M5 DB 10,13,"ENTER ITEM TO SEARCH: $"
DATA ENDS

PRTMSG MACRO MSG
    LEA DX,MSG
    MOV AH,09H
    INT 21H
ENDM

GETDCM MACRO
    MOV AH,01H
    INT 21H
    SUB AL,30H
ENDM

CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX

    PRTMSG M3
    GETDCM
    MOV CL, AL

    LEA SI, STRING
    MOV BL, CL

GETDATA:
    PRTMSG M4
    GETDCM
    MOV [SI], AL
    INC SI
    DEC CL
    JNZ GETDATA
    MOV CL, BL
    LEA SI, STRING
    PRTMSG M5
    GETDCM

UP:
    MOV BL, [SI]
    CMP AL,BL
    JZ FO

    INC SI
    DEC CX
    JNZ UP

    PRTMSG M2
    JMP END1

FO:
    PRTMSG M1

END1:
    MOV AH,4CH
    INT 21H
CODE ENDS
END START
